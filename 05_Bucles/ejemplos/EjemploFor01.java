/**
 * Ejemplo de bucle For
 * Imprimir por pantalla del 1 al 10 
 * 
 * @author Alejandro Barrionuevo Rosado
 */
public class EjemploFor01 {
  public static void main(String[] args) {
    for (int i = 1; i <= 10; i++ ){
      System.out.println(i);
    }
  }
}

